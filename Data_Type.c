#include<stdio.h>

//Premitive Data types

int main()
{
    int No = 10;

    float fValue = 3.14;

    double dValue = 3.4100;

    char cValue = 'A';

    printf("Integer is %d\n",No) ;
    printf("Float is %f\n",fValue) ;
    printf("Double is %lf\n",dValue) ;
    printf("Character is %c\n",cValue) ;

}